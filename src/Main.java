public class Main {
    public static void main ( String[] args) {
        //FOR LOOP
        for (int i = 1; i<= 5; i++) {
            System.out.println ("FOR LOOP REPEAT: " + i);

        }
        //WHILE LOOP
        int k = 1;
        while (k <= 5) {
            System.out.println("WHILE LOOP REPEAT: " + k);
            k++;
        }
        // DO-WHILE LOOP
        int m = 1;
        do {
            System.out.println(" DO-WHILE LOOP REPEAT: " + m);
            m++;
        } while ( m <= 5);

    }

}
